#include "libft.h"

int		ft_atoi(const char *str)
{
	unsigned long long	x;
	int					n;
	int					i;

	i = 0;
	n = 1;
	x = 0;
	while (str[i] == '\n' || str[i] == '\t' || str[i] == '\r' ||
		str[i] == '\v' || str[i] == '\f' || str[i] == ' ')
		i++;
	if (str[i] == '-')
		n = -1;
	if (str[i] == '-' || str[i] == '+')
		i++;
	while (str[i] >= '0' && str[i] <= '9')
	{
		x = (x * 10) + (str[i] - 48);
		i++;
	}
	if (x > 9223372036854775807)
		return ((n == 1) ? -1 : 0);
	x *= n;
	return ((int)x);
}
