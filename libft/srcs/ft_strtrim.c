#include "libft.h"

char	*ft_strtrim(char const *s)
{
	int i;
	int n;

	if (s == NULL)
		return (NULL);
	i = 0;
	while (s[i] == ' ' || s[i] == '\n' || s[i] == '\t')
		i++;
	n = ft_strlen(s) - 1;
	while (s[n] == ' ' || s[n] == '\n' || s[n] == '\t')
		n--;
	n = n - i + 1;
	if (n < 0)
		n = 0;
	return (ft_strsub(s, i, n));
}
