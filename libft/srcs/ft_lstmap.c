#include "libft.h"

t_list	*ft_lstmap(t_list *lst, t_list *(*f)(t_list *elem))
{
	t_list *new;
	t_list *start;

	if ((new = f(lst)) == NULL)
		return (NULL);
	start = new;
	while (lst->next)
	{
		if ((new->next = f(lst->next)) == NULL)
			return (NULL);
		new = new->next;
		lst = lst->next;
	}
	return (start);
}
