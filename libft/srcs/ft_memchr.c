#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	size_t			i;
	unsigned char	uc;
	char			*str;

	str = (char*)s;
	uc = (unsigned char)c;
	i = 0;
	while (i < n)
	{
		if ((unsigned char)str[i] == uc)
			return ((void*)&str[i]);
		i++;
	}
	return (NULL);
}
