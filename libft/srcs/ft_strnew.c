#include "libft.h"

char	*ft_strnew(size_t size)
{
	char	*str;
	size_t	i;

	size++;
	if ((str = (char*)malloc(sizeof(char) * size)) == NULL)
		return (NULL);
	i = 0;
	while (i < size)
	{
		str[i] = 0;
		i++;
	}
	return (str);
}
