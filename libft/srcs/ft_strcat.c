#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	int i;
	int n;

	i = ft_strlen(s1);
	n = 0;
	while (s2[n])
	{
		s1[i + n] = s2[n];
		n++;
	}
	s1[i + n] = 0;
	return (s1);
}
