#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t i;
	size_t x;

	i = ft_strlen(s1);
	x = 0;
	while (x < n && s2[x])
	{
		s1[i + x] = s2[x];
		x++;
	}
	s1[i + x] = 0;
	return (s1);
}
